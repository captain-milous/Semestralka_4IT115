package cz.vse.semestralka_4it115.ui.cmd;

/**
 * Enum representing possible look actions a player can perform in a room.
 * DETAIL to get detailed description of the room, EXITS to list available exits,
 * ITEMS to view items present in the room, and PEOPLE to list characters present.
 *
 * @author Miloš Tesař
 * @version BETA
 * @since 2025-04-03
 */
public enum LookActions {
    DETAIL, EXITS, ITEMS, PEOPLE
}
